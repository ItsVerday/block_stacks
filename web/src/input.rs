use common::Button;

pub fn key_name_to_common(key: &str) -> Option<Button> {
	match key {
		"backspace" => Some(Button::KeyBackspace),
		"tab" => Some(Button::KeyTab),
		"enter" => Some(Button::KeyReturn),
		"escape" => Some(Button::KeyEscape),
		" " => Some(Button::KeySpace),
		"!" => Some(Button::KeyExclaim),
		"\"" => Some(Button::KeyDoubleQuote),
		"#" => Some(Button::KeyHash),
		"$" => Some(Button::KeyDollar),
		"%" => Some(Button::KeyPercent),
		"&" => Some(Button::KeyAmpersand),
		"'" => Some(Button::KeyQuote),
		"(" => Some(Button::KeyLeftParen),
		")" => Some(Button::KeyRightParen),
		"*" => Some(Button::KeyAsterisk),
		"+" => Some(Button::KeyPlus),
		"," => Some(Button::KeyComma),
		"-" => Some(Button::KeyMinus),
		"." => Some(Button::KeyPeriod),
		"/" => Some(Button::KeySlash),
		"0" => Some(Button::Key0),
		"1" => Some(Button::Key1),
		"2" => Some(Button::Key2),
		"3" => Some(Button::Key3),
		"4" => Some(Button::Key4),
		"5" => Some(Button::Key5),
		"6" => Some(Button::Key6),
		"7" => Some(Button::Key7),
		"8" => Some(Button::Key8),
		"9" => Some(Button::Key9),
		":" => Some(Button::KeyColon),
		";" => Some(Button::KeySemicolon),
		"<" => Some(Button::KeyLess),
		"=" => Some(Button::KeyEquals),
		">" => Some(Button::KeyGreater),
		"?" => Some(Button::KeyQuestion),
		"@" => Some(Button::KeyAt),
		"[" => Some(Button::KeyLeftBracket),
		"\\" => Some(Button::KeyBackslash),
		"]" => Some(Button::KeyRightBracket),
		"^" => Some(Button::KeyCaret),
		"_" => Some(Button::KeyUnderscore),
		"`" => Some(Button::KeyBackquote),
		"a" => Some(Button::KeyA),
		"b" => Some(Button::KeyB),
		"c" => Some(Button::KeyC),
		"d" => Some(Button::KeyD),
		"e" => Some(Button::KeyE),
		"f" => Some(Button::KeyF),
		"g" => Some(Button::KeyG),
		"h" => Some(Button::KeyH),
		"i" => Some(Button::KeyI),
		"j" => Some(Button::KeyJ),
		"k" => Some(Button::KeyK),
		"l" => Some(Button::KeyL),
		"m" => Some(Button::KeyM),
		"n" => Some(Button::KeyN),
		"o" => Some(Button::KeyO),
		"p" => Some(Button::KeyP),
		"q" => Some(Button::KeyQ),
		"r" => Some(Button::KeyR),
		"s" => Some(Button::KeyS),
		"t" => Some(Button::KeyT),
		"u" => Some(Button::KeyU),
		"v" => Some(Button::KeyV),
		"w" => Some(Button::KeyW),
		"x" => Some(Button::KeyX),
		"y" => Some(Button::KeyY),
		"z" => Some(Button::KeyZ),
		"delete" => Some(Button::KeyDelete),
		"up" | "arrowup" => Some(Button::KeyUp),
		"left" | "arrowleft" => Some(Button::KeyLeft),
		"down" | "arrowdown" => Some(Button::KeyDown),
		"right" | "arrowright" => Some(Button::KeyRight),
		_ => None
	}	
}